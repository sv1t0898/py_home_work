print("Задача 24")

bushes = [random.randint(1, 10) for i in range(int(input("Введите количество кустов: ")))]
print(bushes)
def max_berries(list):
    result = 0
    for i in range(len(list)):
        if result < (list[i-2] + list[i-1] + list[i]):
            result = list[i-2] + list[i-1] + list[i]
    return result

print(max_berries(bushes))