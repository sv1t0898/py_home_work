def get_list(len):
    return [random.randint(1, 30) for i in range(len)]

set1 = get_list(int(input("n: ")))
set2 = get_list(int(input("m: ")))

print(set1)
print(set2)

def repeat_num(set1, set2):
    result = []
    for i in range(len(set1)):
        for n in range(len(set2)):
            if set1[i] == set2[n]:
                if set1[i] not in result:
                    result.append(set1[i])
    return result

l = repeat_num(set1, set2)

def sort_list(list):
    for i in range(len(list)):
        for n in range(len(list) - 1):
            if list[n] > list[n + 1]:
                list[n], list[n + 1] = list[n + 1], list[n]
    return list

sort_list(l)
print(l)


